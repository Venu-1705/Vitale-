// ─── Types ────────────────────────────────────────────────────────────────────

export type SessionType = '1-on-1' | 'Group' | 'Workshop' | 'Webinar';
export type SessionStatus = 'Scheduled' | 'Starting Soon' | 'Live' | 'Ended';
export type RecordingStatus = 'Available' | 'Processing' | 'None';

export interface ZoomParticipant {
  id: string;
  name: string;
  initials: string;
  color: string;
  joinTime?: string;
  leaveTime?: string;
  durationMinutes?: number;
  attentionScore?: number; // 0-100
}

export interface ZoomSession {
  id: string;
  type: SessionType;
  title: string;
  program?: string;
  date: string;        // "2026-04-25"
  startTime: string;  // "10:00"
  endTime: string;    // "11:00"
  durationMinutes: number;
  status: SessionStatus;
  participants: ZoomParticipant[];
  totalRegistered: number;
  meetingId: string;
  joinUrl: string;
  coHost?: string;
  autoRecord: boolean;
  waitingRoom: boolean;
  recordingUrl?: string;
  recordingStatus?: RecordingStatus;
  chatLog?: string;
  description?: string;
}

// ─── Helpers ──────────────────────────────────────────────────────────────────

function p(id: string, name: string, initials: string, color: string, join?: string, leave?: string, dur?: number, att?: number): ZoomParticipant {
  return { id, name, initials, color, joinTime: join, leaveTime: leave, durationMinutes: dur, attentionScore: att };
}

