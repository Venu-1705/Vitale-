// ─── Types ────────────────────────────────────────────────────────────────────

export type RecipeCategory =
  | 'Breakfast' | 'Lunch' | 'Dinner' | 'Snack'
  | 'Smoothie/Beverage' | 'Dessert' | 'Salad' | 'Soup' | 'Side Dish';

export type DietType = 'Vegetarian' | 'Vegan' | 'Non-Vegetarian' | 'Eggetarian';
export type Cuisine = 'Indian' | 'South Indian' | 'North Indian' | 'Continental' | 'Mediterranean' | 'Mixed';
export type Difficulty = 'Easy' | 'Medium' | 'Hard';

export type IngredientUnit =
  | 'g' | 'ml' | 'tsp' | 'tbsp' | 'cups' | 'piece' | 'handful' | 'pinch' | 'to taste' | 'katori' | 'bowl';

export interface RecipeIngredient {
  id: string;
  group?: string;
  name: string;
  qty: number;
  unit: IngredientUnit;
  calories: number;
  protein: number;
  carbs: number;
  fat: number;
}

export interface RecipeStep {
  id: string;
  text: string;
  timerNote?: string;
}

export interface RecipeNutrition {
  calories: number;
  protein: number;
  carbs: number;
  fiber: number;
  sugar: number;
  fat: number;
  saturatedFat: number;
  sodium: number;
  iron: number;
  calcium: number;
}

export type AllergenKey = 'nuts' | 'dairy' | 'gluten' | 'eggs' | 'soy' | 'shellfish' | 'sesame';
export type HealthTag =
  | 'PCOS-friendly' | 'Diabetic-friendly' | 'Heart-healthy' | 'Anti-inflammatory'
  | 'Keto-compatible' | 'Low-GI' | 'High-fiber' | 'Gut-health' | 'High-protein' | 'Low-calorie';

export interface Recipe {
  id: string;
  title: string;
  description: string;
  category: RecipeCategory;
  dietType: DietType;
  cuisine: Cuisine;
  servings: number;
  prepTime: number; // minutes
  cookTime: number; // minutes
  difficulty: Difficulty;
  ingredients: RecipeIngredient[];
  steps: RecipeStep[];
  proTips?: string;
  nutrition: RecipeNutrition;
  healthTags: HealthTag[];
  allergens: AllergenKey[];
  seasonTags: string[];
  customTags: string[];
  likes: number;
  featured: boolean;
  coachNotes?: string;
  createdBy: string;
  createdByInitials: string;
  createdAt: string;
  // visual — emoji + gradient (used as image placeholder)
  emoji: string;
  bgFrom: string;
  bgTo: string;
}

// ─── INGREDIENT_UNIT options ──────────────────────────────────────────────────

export const INGREDIENT_UNITS: IngredientUnit[] = [
  'g', 'ml', 'tsp', 'tbsp', 'cups', 'piece', 'handful', 'pinch', 'to taste', 'katori', 'bowl',
];

// ─── Helper ───────────────────────────────────────────────────────────────────

function i(id: string, name: string, qty: number, unit: IngredientUnit, cal: number, prot: number, carb: number, fat: number, group?: string): RecipeIngredient {
  return { id, name, qty, unit, calories: cal, protein: prot, carbs: carb, fat, group };
}
function s(id: string, text: string, timerNote?: string): RecipeStep {
  return { id, text, timerNote };
}

